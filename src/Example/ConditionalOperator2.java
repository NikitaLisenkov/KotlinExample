package Example;

public class ConditionalOperator2 {
    public static void main(String[] args) {
        int money = 7;
        if (money > 10) {
            System.out.println("pizza");

        } else if (money < 6) {
            System.out.println("sandwich");

        } else {
            System.out.println("hamburger");
        }
    }
}
